package org.example;

public class Calculator {

    public int add(int a, int b) {
        return a + b;
    }

    public int divide(int a, int b) {
        return a / b;
    }

    public int multi (int a, int b) {
        return a * b;
    }

    public int remove (int a, int b) {
        return a - b;
    }
}